import * as React from "react";

type IconProps = Omit<React.SVGProps<SVGSVGElement>, "width" | "height"> & {
  size?: number | string;
  title?: string;
};

function useA11y(title?: string) {
  const titleId = React.useId();
  return {
    labelledBy: title ? titleId : undefined,
    hidden: title ? undefined : true,
    titleId,
  };
}

function createIcon(
  paths: React.ReactNode,
  {
    viewBox = "0 0 24 24",
    defaultFill = "none",
  }: { viewBox?: string; defaultFill?: string } = {},
) {
  const Icon = React.forwardRef<SVGSVGElement, IconProps>(function Icon(
    { size = 24, title, fill = defaultFill, ...rest },
    ref,
  ) {
    const { labelledBy, hidden, titleId } = useA11y(title);
    return (
      <svg
        ref={ref}
        width={size}
        height={size}
        viewBox={viewBox}
        fill={fill}
        xmlns="http://www.w3.org/2000/svg"
        aria-hidden={hidden}
        aria-labelledby={labelledBy}
        role="img"
        {...rest}
      >
        {title ? <title id={titleId}>{title}</title> : null}
        {paths}
      </svg>
    );
  });
  return React.memo(Icon);
}

